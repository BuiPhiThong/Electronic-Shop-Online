package model;

/**
 *
 * @author BUI TUAN DAT
 */
public class Publisher {
    private int pid;
    private String name,address,phone;

    public Publisher() {
    }

    public Publisher(int pid, String name, String address, String phone) {
        this.pid = pid;
        this.name = name;
        this.address = address;
        this.phone = phone;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
    
    
   
    
}
